#include "../includes/libft.h"

int	powerten(int val)
{
	int	ten;

	ten = 10;
	if (val > 1)
	{
		while (val > 0)
		{
			ten *= 10;
			val--;
		}
	}
	else if (val < 1)
		ten = 1;
	return (ten);
}
