#include "libft.h"

void		*ft_memset(void *str, int c, size_t n)
{
	size_t	i;
	char	*tmp;

	i = 0;
	tmp = (char *)str;
	while (i < n)
	{
		tmp[i] = c;
		i++;
	}
	return (tmp);
}
