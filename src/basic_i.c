#include "libft.h"

int	basic_i(int *i, int *j, size_t sizej, size_t sizei)
{
	if (*j < (int)sizej)
	{
		if (*i < (int)sizei)
			*i += 1;
		else
		{
			*j += 1;
			*i = 0;
		}
		return (1);
	}
	else
		return (0);
}
