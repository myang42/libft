#include "libft.h"

void	basic_i2(int *i, int *j, size_t sizej, size_t sizei)
{
	while (*j < (int)sizej)
	{
		if (*i < (int)sizei)
			*i += 1;
		else
		{
			*j += 1;
			*i = 0;
		}
	}
}
