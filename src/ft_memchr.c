#include "libft.h"

void		*ft_memchr(const void *s, int c, size_t n)
{
	char	*tmp;
	size_t	i;

	i = 0;
	tmp = (char *)s;
	while (i < n)
	{
		if (tmp[i] == (char)c)
			return ((void *)&tmp[i]);
		else
			i++;
	}
	return (NULL);
}
