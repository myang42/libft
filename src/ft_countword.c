#include "libft.h"

size_t	ft_countword(char const *s, char c)
{
	size_t	i;
	size_t	w;

	i = 0;
	w = ((s[i] && s[i] != c) ? 1 : 0);
	while (s[i])
	{
		if (s[i] == c && s[i + 1] != c && s[i + 1] != '\0')
			w++;
		i++;
	}
	return (w);
}
